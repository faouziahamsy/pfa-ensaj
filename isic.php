    <h4>OBJECTIFS DE LA FORMATION</h4>
    <p>
    Les Technologies de l’Information et de la Communication (TIC) jouent un rôle majeur dans la compétitivité des entreprises et dans l’efficacité des administrations et des services publics (santé, éducation, sécurité). Le secteur des TIC est devenu un segment majeur de l’économie des principaux pays industrialisés et les perspectives de croissances de ce secteur dans notre pays sont par ailleurs considérables et requiert un grand nombre d’ingénieurs et de chercheurs.
Les entreprises et les organisations marocaines ont de plus en plus besoin de cadres qui seront de véritables agents capables de s’adapter aux changements, d’introduire des nouvelles méthodes d’études et de mettre en œuvre des structures, des réseaux informatiques et des systèmes de télécommunications adaptés à l’évolution du monde technologique et socio-économique.
La formation d’ingénieur en Systèmes d’Information et de Communication couvre tous les domaines des Télécommunications et des Réseaux Informatiques de tout type. Leur savoir-faire repose sur la conception et le développement des équipements matériels et des logiciels pour les Télécommunications et les Réseaux et sur la maîtrise des techniques nécessaires à la mise en œuvre des services de l’information et de la communication pour produire, manipuler, convertir, stocker, gérer, transmettre et retrouver l’information.
    </p>
    <h4>COMPETENCES A ACQUERIR :</h4>
    <p>
    Cette formation permettra aux étudiants d’acquérir et de renforcer leurs compétences dans les domaines suivants:
        <li>Les nouvelles technologies de télécommunications</li>
        <li>Les réseaux informatiques hauts débits, sans fils et mobiles</li>
        <li>Les systèmes d’information</li>
        <li>Les systèmes embarqués et mobile</li>
        <li>La gestion des services,la capacité de décision et de dimensionnement des SI</li>
    </p>
